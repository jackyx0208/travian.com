    public static function getNearbyXY($x, $y, $max_distance)
    {
        $angle = atan2($y, $x);
        $r = hypot($x, $y);
        $half = $max_distance / 2;
        $pointRadius = mt_rand($r - $half, $r + $half);
        $point = [
            'x' => ceil(cos($angle) * $pointRadius),
            'y' => ceil(sin($angle) * $pointRadius),
        ];
        return $point;
    }

    public static function getXYAngle($x, $y)
    {
