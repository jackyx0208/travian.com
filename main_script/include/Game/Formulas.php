    }

    private static function uResearchCostCoeffs($u)
    {
        $u = unitIdToNr($u) - 1;
        $stdCost = ["b" => [100, 100, 200, 160], "k" => [6, 4, 8, 6], "t" => 3];
        $adminCost = [
            "b" => [500, 200, 400, 160],
            "k" => [0.5, 0.5, 0.8, 0.6],
            "t" => 0.25,
        ];
        $nullCost = ["k" => 0, "b" => 0, "t" => 0];
        if ($u < 8) {
            return $stdCost;
        } else if ($u == 8) {
            return $adminCost;
        }

        return $nullCost;
    }

    public static function uTrainingCost($u, $big = FALSE)
    {
