    public static function uUpkeep($u, $hdp = 0, $isBattle = FALSE)
    {
        if ($u == 99) {
            return 0;
        }
        if ($u == 98) {
            return 6;
        }
        $race = unitIdToTribe($u) - 1;
        if ($race === 3 && !$isBattle) {
            return 0;
        } //nature units doesn't consume
        $u = unitIdToNr($u) - 1;
        $cu = self::$data['units'][$race][$u]['cu'];
        if ($race == 0 && 3 <= $u && $u <= 5) {
            if ($u == 3 && $hdp >= 10) {
                --$cu;
            }
            if ($u == 4 && $hdp >= 15) {
                --$cu;
            }
            if ($u == 5 && $hdp >= 20) {
                --$cu;
            }
        }
        return $cu;
    }

