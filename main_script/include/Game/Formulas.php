    private static function trickyRounding($value, $lvl)
    {
        if ($lvl < 5) {
            $round_size = 10;
        } else if ($lvl < 10) {
            $round_size = 50;
        } else {
            $round_size = 100;
        }

        return round($value / $round_size) * $round_size;
    }

